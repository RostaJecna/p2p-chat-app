namespace Peer2P.Library.Console.Messaging;

/// <summary>
///     Represents communication protocols for logging purposes.
/// </summary>
public enum LogProtocol
{
    /// <summary>
    ///     Transmission Control Protocol.
    /// </summary>
    Tcp,

    /// <summary>
    ///     User Datagram Protocol.
    /// </summary>
    Udp,

    /// <summary>
    ///     Hypertext Transfer Protocol.
    /// </summary>
    Http
}